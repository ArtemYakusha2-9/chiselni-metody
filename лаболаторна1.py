# ‚àö66 = 8.12 7/3 = 2.33
import math as m
x_actual_sqrt = m.sqrt(66)  # —Ç–æ—á–Ω–µ –∑–Ω–∞—á–µ–Ω–Ω—è –¥–ª—è ‚àö66
x_actual_fraction = 7/3  # —Ç–æ—á–Ω–µ –∑–Ω–∞—á–µ–Ω–Ω—è –¥–ª—è 7/3
