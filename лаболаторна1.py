# –≤–∏–≤–µ–¥–µ–Ω–Ω—è —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ñ–≤
print("–∞–±—Å–æ–ª—é—Ç–Ω–∞ –ø–æ—Ö–∏–±–∫–∞ –¥–ª—è ‚àö66:", abs(absolute_error_sqrt))
print("–≤—ñ–¥–Ω–æ—Å–Ω–∞ –ø–æ—Ö–∏–±–∫–∞ –¥–ª—è ‚àö66:", str(abs(relative_error_sqrt)) + '%')
print("–∞–±—Å–æ–ª—é—Ç–Ω–∞ –ø–æ—Ö–∏–±–∫–∞ –¥–ª—è 7/3:", abs(absolute_error_fraction))
print("–≤—ñ–¥–Ω–æ—Å–Ω–∞ –ø–æ—Ö–∏–±–∫–∞ –¥–ª—è 7/3:", str(abs(relative_error_fraction)) + '%')

